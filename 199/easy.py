'''
Takes an input of digits line this:

000000000
111111111
490067715

and prints out:
 _  _  _  _  _  _  _  _  _ 
| || || || || || || || || |
|_||_||_||_||_||_||_||_||_|
                           
  |  |  |  |  |  |  |  |  |
  |  |  |  |  |  |  |  |  |
    _  _  _     _  _     _ 
|_||_|| || ||_   |  |  ||_ 
  |  ||_||_||_|  |  |  | _|
'''


import sys

input_ = '''\
000000000
111111111
490067715'''

digits = {
        '0': [[' ', '_', ' '], ['|', ' ', '|'], ['|', '_', '|']],
        '1': [[' ', ' ', ' '], [' ', ' ', '|'], [' ', ' ', '|']],
        '2': [[' ', '_', ' '], [' ', '_', '|'], ['|', '_', ' ']],
        '3': [[' ', '_', ' '], [' ', '_', '|'], [' ', '_', '|']],
        '4': [[' ', ' ', ' '], ['|', '_', '|'], [' ', ' ', '|']],
        '5': [[' ', '_', ' '], ['|', '_', ' '], [' ', '_', '|']],
        '6': [[' ', ' ', ' '], ['|', '_', ' '], ['|', '_', '|']],
        '7': [[' ', '_', ' '], [' ', ' ', '|'], [' ', ' ', '|']],
        '8': [[' ', ' ', ' '], ['|', '_', '|'], ['|', '_', '|']],
        '9': [[' ', '_', ' '], ['|', '_', '|'], [' ', ' ', '|']]
        }


def print_(array, level):
    for i in range(3):
        sys.stdout.write(array[level][i])

lines = input_.split()

for line in lines:
    for level in range(3):
        for char in line:
            n = digits[char]
            print_(n, level)
        print
